type LanguageContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey) => string;
  actionLabel: (action: Action) => string;
  trendLabel: (trend: HoldingTrend | null | undefined) => string;
  methodologyLabel: (note: string) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

function initialLanguage(): Language {
  if (typeof window === "undefined") return "zh-TW";
  const savedLanguage = window.localStorage.getItem(STORAGE_KEY);
  return savedLanguage === "en" || savedLanguage === "zh-TW" ? savedLanguage : "zh-TW";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(initialLanguage);

  const value = useMemo<LanguageContextValue>(() => {
    const setLanguage = (nextLanguage: Language) => {
      setLanguageState(nextLanguage);
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    };

    return {
      language,
      setLanguage,
      t: (key) => translations[language][key],
      actionLabel: (action) => actionLabels[language][action],
      trendLabel: (trend) => (trend ? trendLabels[language][trend] : "-"),
      methodologyLabel: (note) => (language === "zh-TW" ? methodologyLabels[note] ?? note : note),
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
}
